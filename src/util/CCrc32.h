public:
    /// Compute CRC32 over raw bytes, chaining with previous CRC value
    [[nodiscard]] static auto GetCrc32(
        const void* pData,
        std::uint32_t nLen,
        std::uint32_t dwPrevCRC) -> std::uint32_t
    {
        auto crc = dwPrevCRC ^ 0xFFFFFFFF;
        auto* p = static_cast<const std::uint8_t*>(pData);

        for (std::uint32_t i = 0; i < nLen; ++i)
        {
            crc = s_aTable[(crc ^ p[i]) & 0xFF] ^ (crc >> 8);
        }

        return crc ^ 0xFFFFFFFF;
    }

    /// Convenience: CRC32 a single 32-bit integer
    [[nodiscard]] static auto GetCrc32(
        std::int32_t nValue,
        std::uint32_t dwPrevCRC) -> std::uint32_t
    {
        return GetCrc32(&nValue, 4, dwPrevCRC);
    }

    /// Convenience: CRC32 a single unsigned 32-bit integer
    [[nodiscard]] static auto GetCrc32(
        std::uint32_t nValue,
        std::uint32_t dwPrevCRC) -> std::uint32_t
    {
        return GetCrc32(&nValue, 4, dwPrevCRC);
    }

